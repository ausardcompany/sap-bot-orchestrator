/**
 * CI/CD Workflow Template Definitions
 * Contains inline YAML templates for GitHub Actions and GitLab CI
 */

// ============ Type Definitions ============

export type CIPlatform = 'github' | 'gitlab';
export type TemplateType = 'pr-review' | 'code-check' | 'feature-implement';

export interface CITemplate {
  id: string;
  name: string;
  platform: CIPlatform;
  type: TemplateType;
  description: string;
  outputPath: string;
  content: string;
}

