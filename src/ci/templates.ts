/**
 * GitHub Actions - PR Review Template
 * Triggers on pull_request events to review changes with AI
 */
const githubPrReviewTemplate: CITemplate = {
  id: 'github-pr-review',
  name: 'GitHub Actions - PR Review',
  platform: 'github',
  type: 'pr-review',
  description: 'Automatically review pull requests using SAP AI Core and post feedback as PR comments',
  outputPath: '.github/workflows/ai-pr-review.yml',
  content: `# AI-Powered PR Review Workflow
# This workflow uses Alexi to review pull requests
#
# Setup:
# 1. Add SAP AI Core credentials as repository secrets:
#    - SAP_AICORE_SERVICE_KEY: Your SAP AI Core service key JSON
#    OR for proxy mode:
#    - SAP_PROXY_BASE_URL: URL to your SAP proxy server
#    - SAP_PROXY_API_KEY: API key for proxy authentication (if required)
#
# 2. Ensure the workflow has permission to comment on PRs:
#    Settings > Actions > General > Workflow permissions > Read and write permissions

name: AI PR Review

on:
  pull_request:
    types: [opened, synchronize]

permissions:
  contents: read
  pull-requests: write

jobs:
  ai-review:
    name: AI Code Review
    runs-on: ubuntu-latest
    
    steps:
      - name: Checkout code
        uses: actions/checkout@v4
        with:
          fetch-depth: 0  # Fetch full history for diff analysis
      
      - name: Setup Node.js
        uses: actions/setup-node@v4
        with:
          node-version: '22'
      
      - name: Install Alexi
        run: npm install -g alexi
      
      - name: Get PR diff
        id: diff
        run: |
          # Get the diff between base and head
          git diff origin/\${{ github.base_ref }}...HEAD > pr_diff.txt
          echo "diff_file=pr_diff.txt" >> $GITHUB_OUTPUT
      
      - name: Run AI Review
        id: review
        env:
          AICORE_SERVICE_KEY: \${{ secrets.SAP_AICORE_SERVICE_KEY }}
          SAP_PROXY_BASE_URL: \${{ secrets.SAP_PROXY_BASE_URL }}
          SAP_PROXY_API_KEY: \${{ secrets.SAP_PROXY_API_KEY }}
        run: |
          # Create review prompt
          cat << 'PROMPT' > review_prompt.txt
          Review the following code changes from a pull request.
          
          Provide feedback on:
          1. Code quality and best practices
          2. Potential bugs or issues
          3. Security concerns
          4. Performance implications
          5. Suggestions for improvement
          
          Be constructive and specific. Reference line numbers when applicable.
          Format your response in markdown.
          
          PR Title: \${{ github.event.pull_request.title }}
          PR Description: \${{ github.event.pull_request.body }}
          
          Code Changes:
          PROMPT
          
          cat pr_diff.txt >> review_prompt.txt
          
          # Run alexi and capture output
          alexi --prompt "$(cat review_prompt.txt)" --output review_output.md --non-interactive || true
          
          # Check if review was generated
          if [ -f review_output.md ]; then
            echo "review_generated=true" >> $GITHUB_OUTPUT
          else
            echo "review_generated=false" >> $GITHUB_OUTPUT
            echo "AI review could not be generated" > review_output.md
          fi
      
      - name: Post Review Comment
        if: steps.review.outputs.review_generated == 'true'
        uses: actions/github-script@v7
        with:
          script: |
            const fs = require('fs');
            const review = fs.readFileSync('review_output.md', 'utf8');
            
            const body = \`## AI Code Review
            
            \${review}
            
            ---
            *This review was generated by Alexi*
            \`;
            
            await github.rest.issues.createComment({
              owner: context.repo.owner,
              repo: context.repo.repo,
              issue_number: context.issue.number,
              body: body
            });
`,
};

