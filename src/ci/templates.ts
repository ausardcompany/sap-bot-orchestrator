/**
 * GitLab CI - PR Review Template
 * Triggers on merge request events
 */
const gitlabPrReviewTemplate: CITemplate = {
  id: 'gitlab-pr-review',
  name: 'GitLab CI - MR Review',
  platform: 'gitlab',
  type: 'pr-review',
  description: 'Automatically review merge requests using SAP AI Core and post feedback as comments',
  outputPath: '.gitlab-ci-ai-review.yml',
  content: `# AI-Powered Merge Request Review for GitLab CI
# This pipeline uses Alexi to review merge requests
#
# Setup:
# 1. Add SAP AI Core credentials as CI/CD variables:
#    - SAP_AICORE_SERVICE_KEY: Your SAP AI Core service key JSON
#    OR for proxy mode:
#    - SAP_PROXY_BASE_URL: URL to your SAP proxy server
#    - SAP_PROXY_API_KEY: API key for proxy authentication (if required)
#
# 2. Add this file's content to your .gitlab-ci.yml or include it:
#    include:
#      - local: '.gitlab-ci-ai-review.yml'
#
# 3. Ensure the pipeline has API access for posting comments:
#    - Set GITLAB_TOKEN variable with a token that has api scope

stages:
  - review

ai-mr-review:
  stage: review
  image: node:22
  
  rules:
    - if: $CI_PIPELINE_SOURCE == "merge_request_event"
  
  variables:
    GIT_DEPTH: 0
  
  before_script:
    - npm install -g alexi
    - apt-get update && apt-get install -y curl
  
  script:
    # Get the diff between source and target branch
    - git fetch origin $CI_MERGE_REQUEST_TARGET_BRANCH_NAME
    - git diff origin/$CI_MERGE_REQUEST_TARGET_BRANCH_NAME...HEAD > mr_diff.txt
    
    # Create review prompt
    - |
      cat << 'PROMPT' > review_prompt.txt
      Review the following code changes from a merge request.
      
      Provide feedback on:
      1. Code quality and best practices
      2. Potential bugs or issues
      3. Security concerns
      4. Performance implications
      5. Suggestions for improvement
      
      Be constructive and specific. Reference line numbers when applicable.
      Format your response in markdown.
      
      MR Title: $CI_MERGE_REQUEST_TITLE
      Source Branch: $CI_MERGE_REQUEST_SOURCE_BRANCH_NAME
      Target Branch: $CI_MERGE_REQUEST_TARGET_BRANCH_NAME
      
      Code Changes:
      PROMPT
    
    - cat mr_diff.txt >> review_prompt.txt
    
    # Run AI review
    - alexi --prompt "$(cat review_prompt.txt)" --output review_output.md --non-interactive || true
    
    # Post review as MR comment
    - |
      if [ -f review_output.md ] && [ -n "$GITLAB_TOKEN" ]; then
        # Escape content for JSON
        REVIEW_BODY=$(cat review_output.md | jq -Rs '.')
        curl --request POST \\
          --header "PRIVATE-TOKEN: $GITLAB_TOKEN" \\
          --header "Content-Type: application/json" \\
          --data '{"body": '"$REVIEW_BODY"'}' \\
          "$CI_API_V4_URL/projects/$CI_PROJECT_ID/merge_requests/$CI_MERGE_REQUEST_IID/notes"
        echo "Review posted to merge request"
      else
        echo "Review output:"
        cat review_output.md || echo "No review generated"
      fi
  
  artifacts:
    paths:
      - review_output.md
      - mr_diff.txt
    expire_in: 7 days
    when: always
`,
};

