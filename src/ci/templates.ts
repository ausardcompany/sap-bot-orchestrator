// ============ All Templates Export ============

/**
 * All available CI/CD templates
 */
export const ALL_TEMPLATES: CITemplate[] = [
  githubPrReviewTemplate,
  githubCodeCheckTemplate,
  githubFeatureImplementTemplate,
  gitlabPrReviewTemplate,
  gitlabCodeCheckTemplate,
];

/**
 * Get templates by platform
 */
export function getTemplatesByPlatform(platform: CIPlatform): CITemplate[] {
  return ALL_TEMPLATES.filter(t => t.platform === platform);
}

/**
 * Get templates by type
 */
export function getTemplatesByType(type: TemplateType): CITemplate[] {
  return ALL_TEMPLATES.filter(t => t.type === type);
}

/**
 * Get a specific template
 */
export function getTemplate(platform: CIPlatform, type: TemplateType): CITemplate | undefined {
  return ALL_TEMPLATES.find(t => t.platform === platform && t.type === type);
}
